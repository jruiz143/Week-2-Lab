#A car is traveling 70 miles per hour. Write a program that displays:
#The distance traveled in 6 hrs, 10hrs, and 15 hrs.

speed= 70 #mph
distance1= speed* 6 #hours
distance2= speed* 10 #hours
distance3= speed* 15 #hours
#Display the different distances traveled
print (f"The car traveled {distance1: .2f}' miles in 6 hours.")
print (f"The car traveled {distance2: .2f}' miles in 6 hours.")
print (f"The car traveled {distance3: .2f}' miles in 6 hours.")